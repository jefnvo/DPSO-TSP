public class Velocity {
    int x1;
    int x2;
    double alfa;
    double beta;

    public Velocity(int x1, int x2, double alfa, double beta) {
        this.x1 = x1;
        this.x2 = x2;
        this.alfa = alfa;
        this.beta = beta;
    }


    public int getX1() {
        return x1;
    }

    public void setX1(int x1) {
        this.x1 = x1;
    }

    public int getX2() {
        return x2;
    }

    public void setX2(int x2) {
        this.x2 = x2;
    }


}
